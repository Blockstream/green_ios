import Foundation
import UIKit

extension UIColor {
    class func customLightGreen() -> UIColor {
        return UIColor(red:89.0/255.0, green:173.0/255.0, blue:87.0/255.0, alpha:1);
    }
    class func customLightGray() -> UIColor {
        return UIColor(red:137.0/255.0, green:137.0/255.0, blue:137.0/255.0, alpha:1);
    }

    class func customMatrixGreen() -> UIColor {
        return UIColor(red:0.0/255.0, green:180.0/255.0, blue:90.0/255.0, alpha:1);
    }

    class func customTitaniumDark() -> UIColor {
        return UIColor(red:31.0/255.0, green:34.0/255.0, blue:44.0/255.0, alpha:1);
    }

    class func customTitaniumMedium() -> UIColor {
        return UIColor(red:60.0/255.0, green:69.0/255.0, blue:76.0/255.0, alpha:1);
    }

    class func customTitaniumLight() -> UIColor {
        return UIColor(red:100.0/255.0, green:109.0/255.0, blue:115.0/255.0, alpha:1);
    }
}
